package com.hjy.web.lol;

public class LolVO {
	private int lNo;
	private String lId;
	private String lName;
	private String lPassword;
	private String lEmail;
	private String lBirth;
	private String lPost;
	private String lAddress;
	private int lRegDate;
	
	public String getlAddress() {
		return lAddress;
	}
	public void setlAddress(String lAddress) {
		this.lAddress = lAddress;
	}

	public int getlNo() {
		return lNo;
	}
	public void setlNo(int lNo) {
		this.lNo = lNo;
	}
	public String getlId() {
		return lId;
	}
	public void setlId(String lId) {
		this.lId = lId;
	}
	public String getlName() {
		return lName;
	}
	public void setlName(String lName) {
		this.lName = lName;
	}
	public String getlPassword() {
		return lPassword;
	}
	public void setlPassword(String lPassword) {
		this.lPassword = lPassword;
	}
	public String getlEmail() {
		return lEmail;
	}
	public void setlEmail(String lEmail) {
		this.lEmail = lEmail;
	}
	public String getlBirth() {
		return lBirth;
	}
	public void setlBirth(String lBirth) {
		this.lBirth = lBirth;
	}
	public String getlPost() {
		return lPost;
	}
	public void setlPost(String lPost) {
		this.lPost = lPost;
	}

	public int getlRegDate() {
		return lRegDate;
	}
	public void setlRegDate(int lRegDate) {
		this.lRegDate = lRegDate;
	}

}
